void main() {
  int firstInteger = 25;
  int secondInteger = 38;

  // Calculate the sum of the two integers
  int sum = firstInteger + secondInteger;
  print("Sum of the above two integers = $sum");

  // Check if the sum is odd or even
  if (sum % 2 == 0) {
    print("$sum is even number.");
  } else {
    print("$sum is odd number.");
  }
}
